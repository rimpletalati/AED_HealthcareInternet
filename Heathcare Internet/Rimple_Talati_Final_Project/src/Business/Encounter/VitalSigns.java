package Business.Encounter;

/**
 *
 * @author Rimple
 */
public class VitalSigns {
    
    private float systolicBloodPressure;
    private float heartRate;
    private float temperatue;
    private float weight;
    private float respiratoryRate;

    public float getSystolicBloodPressure() {
        return systolicBloodPressure;
    }

    public void setSystolicBloodPressure(float systolicBloodPressure) {
        this.systolicBloodPressure = systolicBloodPressure;
    }

    public float getHeartRate() {
        return heartRate;
    }

    public void setHeartRate(float heartRate) {
        this.heartRate = heartRate;
    }

    public float getTemperatue() {
        return temperatue;
    }

    public void setTemperatue(float temperatue) {
        this.temperatue = temperatue;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public float getRespiratoryRate() {
        return respiratoryRate;
    }

    public void setRespiratoryRate(float respiratoryRate) {
        this.respiratoryRate = respiratoryRate;
    }
    
    
}
