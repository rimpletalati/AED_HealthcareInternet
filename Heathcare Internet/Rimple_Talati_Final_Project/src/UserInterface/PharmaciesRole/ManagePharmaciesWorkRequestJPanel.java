    private void viewPateintsJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewPateintsJButtonActionPerformed
        int selectedRow = manageWorkRequestsJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        WorkRequest wr = (WorkRequest) manageWorkRequestsJTable.getValueAt(selectedRow, 0);
        if (wr instanceof PharmacyWorkRequest) {
            PharmacyWorkRequest pwr = (PharmacyWorkRequest) wr;
            patientDetailsJPanel.setVisible(true);
            patientNameJTextField.setText(pwr.getPatient().getFirstName() + " " + pwr.getPatient().getLastName());
            mobilePhoneJjTextField.setText(String.valueOf(pwr.getPatient().getMobilePhone()));
            addressJTextField.setText(pwr.getPatient().getHomeAddress());
            cityJTextField.setText(pwr.getPatient().getCity());
            drugsJTextArea.setText(pwr.getDrugs().getDrugName());
        }

    }//GEN-LAST:event_viewPateintsJButtonActionPerformed

    private void processWorkJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processWorkJButtonActionPerformed
        int selectedRow = manageWorkRequestsJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        WorkRequest wr = (WorkRequest) manageWorkRequestsJTable.getValueAt(selectedRow, 0);
        if (wr instanceof PharmacyWorkRequest) {
            PharmacyWorkRequest pwr = (PharmacyWorkRequest) wr;
            pwr.setStatus("Your order has been dispatched");
            pwr.setPharmicistName(enterprise.getName());
            JOptionPane.showMessageDialog(null,"Succesfully, processed");
        }

    }//GEN-LAST:event_processWorkJButtonActionPerformed


