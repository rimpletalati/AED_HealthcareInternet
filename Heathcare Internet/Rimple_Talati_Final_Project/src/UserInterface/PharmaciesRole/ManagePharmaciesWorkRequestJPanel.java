    private void populatePatients() {
        DefaultTableModel model = (DefaultTableModel) manageWorkRequestsJTable.getModel();
        model.setRowCount(0);
        for (WorkRequest wr : enterprise.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof PharmacyWorkRequest) {
                PharmacyWorkRequest pwr = (PharmacyWorkRequest) wr;
                Object[] row = new Object[5];
                row[0] = pwr;
                row[1] = pwr.getPatient().getFirstName() + " " + pwr.getPatient().getLastName();
                row[2] = pwr.getSender().getFirstName() + " " + pwr.getSender().getLastName();
                row[3] = pwr.getEnterprise();
                row[4] = pwr.getStatus();
                model.addRow(row);

            }
        }
    }

