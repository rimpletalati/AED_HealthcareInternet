    private void viewDetailsJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsJButtonActionPerformed

        int selectedRow = manageEncountersJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        Encounter e = (Encounter) manageEncountersJTable.getValueAt(selectedRow, 0);
        
        ViewEncounterDetailsJPanel viewEncounterDetailsJPanel = new ViewEncounterDetailsJPanel(userProcessContainer, e);
        userProcessContainer.add("viewEncounterDetailsJPanel", viewEncounterDetailsJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_viewDetailsJButtonActionPerformed


