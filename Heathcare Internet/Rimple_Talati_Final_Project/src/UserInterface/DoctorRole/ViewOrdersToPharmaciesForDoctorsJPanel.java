    private void sendJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendJButtonActionPerformed
        int selectedRow = manageTableJTable.getSelectedRow();
        String feedback = feedbackJTextArea.getText();
        if("".equals(feedback)){
            JOptionPane.showMessageDialog(null, "Please write the feedback");
            return;
        }
        PharmacyWorkRequest pwr = (PharmacyWorkRequest) manageTableJTable.getValueAt(selectedRow, 0);
        Drugs d = (Drugs)pwr.getDrugs();
        PharmaceuticalCompaniesEnterprise pce = (PharmaceuticalCompaniesEnterprise)d.getManufacturingCo();

        FeedbackToPCWorkRequest request = new FeedbackToPCWorkRequest();
        request.setMessage(feedback);
        request.setSender(doctor);
        request.setDrug(d);
        pce.getWorkQueue().getWorkRequestList().add(request);
        JOptionPane.showMessageDialog(null,"Your feedback has been sent");
        feedbackJPanel.setVisible(false);
    }//GEN-LAST:event_sendJButtonActionPerformed

    private void viewGuidelinesJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewGuidelinesJButtonActionPerformed
        
        int selectedRow = manageTableJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        //Drugs d = (Drugs)manageTableJTable.getValueAt(selectedRow,3);
        PharmacyWorkRequest pwr = (PharmacyWorkRequest) manageTableJTable.getValueAt(selectedRow, 0);
        Drugs d = (Drugs)pwr.getDrugs();
        ViewGuidelinesPag1JPanel viewGuidelinesPag1JPanel = new ViewGuidelinesPag1JPanel(userProcessContainer, d);
        userProcessContainer.add("viewGuidelinesPag1JPanel", viewGuidelinesPag1JPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_viewGuidelinesJButtonActionPerformed


