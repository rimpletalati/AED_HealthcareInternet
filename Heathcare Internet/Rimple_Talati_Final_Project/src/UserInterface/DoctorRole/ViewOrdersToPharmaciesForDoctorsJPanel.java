    private void populateDrugs() {
        DefaultTableModel model = (DefaultTableModel) manageTableJTable.getModel();
        model.setRowCount(0);
        for (WorkRequest wr : doctor.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof PharmacyWorkRequest) {
                PharmacyWorkRequest pwr = (PharmacyWorkRequest) wr;
                Object[] row = new Object[7];
                row[0] = pwr;
                row[1] = pwr.getPatient().getFirstName() + " " + pwr.getPatient().getLastName();
                row[2] = pwr.getEnterprise();
                row[3] = pwr.getDrugs().getDrugID();
                row[4] = pwr.getDrugs().getDrugName();
                row[5] = pwr.getPharmicistName();
                row[6] = pwr.getStatus();
                model.addRow(row);

            }
        }
    }
