    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) manageTableJTable.getModel();
        model.setRowCount(0);
        for (WorkRequest wr : doctor.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof AssignEmergencyWorkRequest) {
                AssignEmergencyWorkRequest aewr = (AssignEmergencyWorkRequest)wr;

                Object[] row = new Object[4];
                row[0] = aewr;
                row[1] = aewr.getMRNo();
                row[2] = aewr.getPatientName();
                row[3] = aewr.getEnterprise();

                model.addRow(row);

            }
        }
    }

