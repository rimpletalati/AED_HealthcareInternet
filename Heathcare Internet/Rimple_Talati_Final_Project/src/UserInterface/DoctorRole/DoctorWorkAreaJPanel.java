    private void sendEnquiryJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendEnquiryJButtonActionPerformed

        int selectedRow = manageTableJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        int MRNo = (int) manageTableJTable.getValueAt(selectedRow, 1);
        Enterprise SenderEnterprise = (Enterprise) manageTableJTable.getValueAt(selectedRow, 3);
        WorkRequest wr = (WorkRequest)manageTableJTable.getValueAt(selectedRow,0);
        String message = messageJTextField.getText();
        if ("".equals(message)) {
            JOptionPane.showMessageDialog(null, "Please write a message");
            return;
        }
        EnquireEmergencyWorkRequest eewr = new EnquireEmergencyWorkRequest();
        eewr.setDoctor(doctor);
        eewr.setMrNo(MRNo);
        eewr.setEnterprise(SenderEnterprise);
        eewr.setMessage(message);
        doctor.getWorkQueue().getWorkRequestList().add(eewr);
        for (NetworkCountries networkCountries : ecosystem.getNetworkListsCountries()) {
            for (NetworkStates networkStates : networkCountries.getNetworkListStates()) {
                for (NetworkCities networkCities : networkStates.getNetworkListCities()) {
                    for (Enterprise enterprise : networkCities.getEnterpriseDirectory().getEnterpriseList()) {
                        if (enterprise instanceof HospitalEnterprise || enterprise instanceof ClinicEnterprise) {
                            enterprise.getWorkQueue().getWorkRequestList().add(eewr);
                            
                        }
                    }
                }
            }
        }
        JOptionPane.showMessageDialog(null,"Enquiry sent.");
        messageJTextField.setText("");
        doctor.getWorkQueue().getWorkRequestList().remove(wr);
        populateTable();
    }//GEN-LAST:event_sendEnquiryJButtonActionPerformed

