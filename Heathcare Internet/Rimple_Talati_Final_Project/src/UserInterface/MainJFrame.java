    private void loginJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJButtonActionPerformed

        String username = userNameJTextField.getText();
        char[] passwordCharArray = passwordJField.getPassword();
        String password = String.valueOf(passwordCharArray);

        UserAccount userAccount = ecosystem.getUserAccountDirectory().authenticateUser(username, password);

        Enterprise inEnterprise = null;
        Organization inOrganization = null;

        if (userAccount == null) {
            userAccount = ecosystem.getAdminAuthority().getUserAccountDirectory().authenticateUser(username, password);

            if (userAccount == null) {
                userAccount = ecosystem.getWorldFDA().getUserAccountDirectory().authenticateUser(username, password);
                if (userAccount == null) {
                    userAccount = ecosystem.getManageDiseaseAdmin().getUserAccountDirectory().authenticateUser(username, password);
                    if (userAccount == null) {

                        for (NetworkCountries networkCountries : ecosystem.getNetworkListsCountries()) {
                            for (NetworkStates networkStates : networkCountries.getNetworkListStates()) {
                                for (NetworkCities networkCities : networkStates.getNetworkListCities()) {

                                    //step 2.1: check against each enterprise
                                    for (Enterprise enterprise : networkCities.getEnterpriseDirectory().getEnterpriseList()) {
                                        userAccount = enterprise.getUserAccountDirectory().authenticateUser(username, password);
                                        if (userAccount == null) {
                                            //check again each organization inside that enterprise
                                            for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                                                userAccount = organization.getUserAccountDirectory().authenticateUser(username, password);
                                                if (userAccount != null) {
                                                    inEnterprise = enterprise;
                                                    inOrganization = organization;
                                                    break;
                                                }
                                            }
                                        } else {
                                            inEnterprise = enterprise;
                                            break;
                                        }
                                        if (inOrganization != null) {
                                            break;
                                        }
                                    }
                                    if (inEnterprise != null) {
                                        break;
                                    }
                                }
                                if (inEnterprise != null) {
                                    break;
                                }
                            }
                            if (inEnterprise != null) {
                                break;
                            }
                        }
                    }
                }
            }

        }

        if (userAccount == null) {
            JOptionPane.showMessageDialog(null, "Invalid credentials");
        } else {
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("workArea", userAccount.getRole().createWorkArea(userProcessContainer, userAccount, inOrganization, inEnterprise, ecosystem));
            layout.next(userProcessContainer);
        }

    }//GEN-LAST:event_loginJButtonActionPerformed

    private void logoutJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logoutJButtonActionPerformed
        userProcessContainer.removeAll();
        userNameJTextField.setText(null);
        passwordJField.setText(null);
        db4OUtil.storeSystem(ecosystem);
    }//GEN-LAST:event_logoutJButtonActionPerformed

