    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        String reason = reasonJTextArea.getText();
        int selectedRow = manageDrugsJTable.getSelectedRow();
        Drugs drugs = (Drugs) manageDrugsJTable.getValueAt(selectedRow, 0);

        if ("".equals(reason)) {
            JOptionPane.showMessageDialog(null, "Please write a reason to call back");
            return;
        }

        CallBackDrugWorkRequest request = new CallBackDrugWorkRequest();
        request.setMessage(reason);
        request.setDrug(drugs);
        request.setEnterprise(enterprise);
        request.getDrug().setIsApproved("Call Back");
        enterprise.getWorkQueue().getWorkRequestList().add(request);
        if (enterprise instanceof PharmaceuticalCompaniesEnterprise) {
            PharmaceuticalCompaniesEnterprise pce = (PharmaceuticalCompaniesEnterprise) enterprise;
            for (PharmaciesEnterprise pe : pce.getPharmaciesEnterpriseList()) {
                pe.getWorkQueue().getWorkRequestList().add(request);
            }
        }

        reasonJPanel.setVisible(false);
        reasonJTextArea.setText("");
    }//GEN-LAST:event_submitJButtonActionPerformed


