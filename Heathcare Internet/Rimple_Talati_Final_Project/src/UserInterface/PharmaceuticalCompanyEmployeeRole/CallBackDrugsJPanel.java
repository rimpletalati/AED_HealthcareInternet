    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) manageDrugsJTable.getModel();
        model.setRowCount(0);
        if (enterprise instanceof PharmaceuticalCompaniesEnterprise) {
            PharmaceuticalCompaniesEnterprise pce = (PharmaceuticalCompaniesEnterprise) enterprise;
            for (Drugs d : pce.getDrugsList()) {
                if(d.getIsApproved() == null ? Drugs.Approved_Status == null : d.getIsApproved().equals(Drugs.Approved_Status)){
                Object[] row = new Object[3];
                row[0] = d;
                row[1] = d.getDrugName();
                row[2] = d.getIsApproved();

                model.addRow(row);
                }
            }
        }

    }

