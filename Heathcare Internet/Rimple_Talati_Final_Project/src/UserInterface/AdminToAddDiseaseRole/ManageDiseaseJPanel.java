    private void poptable() {
        DefaultTableModel model = (DefaultTableModel) manageDiseaseJTable.getModel();
        model.setRowCount(0);
        for (Disease disease : manageDiseaseAdmin.getDiseaseList()) {
            Object[] row = new Object[4];
            row[0] = disease.getIcd9Code();
            row[1] = disease;
            row[2] = disease.getSymptoms();
            row[3] = disease.getDescription();
            model.addRow(row);
        }
    }

