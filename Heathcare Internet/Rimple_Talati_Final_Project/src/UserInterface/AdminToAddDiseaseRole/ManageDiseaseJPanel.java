    private void addDiseaseJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDiseaseJButtonActionPerformed

        try {
            String icd9Code = icd9CodeJTextField.getText().trim();
            String diseaseName = diseaseNameJTextField.getText().trim();
            String symptoms = symptomsJTextArea.getText();
            String description = descriptionJTextField.getText().trim();

            if (("".equals(icd9Code)) || ("".equals(diseaseName)) || ("".equals(symptoms)) || ("".equals(description))) {
                JOptionPane.showMessageDialog(null, "Please do not keep any values empty.");
                return;
            }
            Disease d = new Disease();
            d.setIcd9Code(Float.parseFloat(icd9Code));
            d.setDiseaseName(diseaseName);
            d.setSymptoms(symptoms);
            d.setDescription(description);
            manageDiseaseAdmin.getDiseaseList().add(d);
            poptable();

            icd9CodeJTextField.setText("");
            diseaseNameJTextField.setText("");
            symptomsJTextArea.setText("");
            descriptionJTextField.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please use valid ICD9 - Code");
        }
    }//GEN-LAST:event_addDiseaseJButtonActionPerformed


