    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        try {
            String patientMRNO = MRNOJTextField.getText().trim();
            String patientName = patientNameJTextField.getText().trim();
            String message = messageJTextArea.getText();
            if (("".equals(patientMRNO)) || ("".equals(patientName)) || ("".equals(message))) {
                JOptionPane.showMessageDialog(null, "Please do not keep patient details empty.");
                return;
            }
            int selectedRow = manageDoctorsJTable.getSelectedRow();
            if (selectedRow < 0) {
                JOptionPane.showMessageDialog(null, "Please select a row");
                return;
            }

            Doctor doctor = (Doctor) manageDoctorsJTable.getValueAt(selectedRow, 1);
            AssignEmergencyWorkRequest aewr = new AssignEmergencyWorkRequest();
            aewr.setMRNo(Integer.parseInt(patientMRNO));

            Pattern pattern = Pattern.compile("[a-zA-Z ]+");
            Matcher name = pattern.matcher(patientName);
            if (name.matches()) {
                aewr.setPatientName(patientName);
            } else {
                JOptionPane.showMessageDialog(null, "only alphabets are allowed for patient name");
                return;
            }
            aewr.setMessage(message);
            aewr.setEnterprise(enterprise);
            doctor.getWorkQueue().getWorkRequestList().add(aewr);
            MRNOJTextField.setText("");
            patientNameJTextField.setText("");
            messageJTextArea.setText("");
            JOptionPane.showMessageDialog(null,"Doctor has been assigned to this patient.");
            submitJButton.setEnabled(false);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please use numbers for MR No.");
        }
    }//GEN-LAST:event_submitJButtonActionPerformed


