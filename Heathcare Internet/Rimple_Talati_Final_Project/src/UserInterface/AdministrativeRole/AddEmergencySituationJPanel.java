    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) manageDoctorsJTable.getModel();
        model.setRowCount(0);
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof DoctorOrganization) {
                Object[] row = new Object[4];
                for (Person p : organization.getPersonDirectory().getPersonList()) {
                    if (p instanceof Doctor) {
                        Doctor d = (Doctor) p;
                        row[0] = d.getFirstName() + " " + d.getLastName();
                        row[1] = d;
                        row[2] = d.getLicenseNo();
                        row[3] = d.getSpecialization();

                        model.addRow(row);

                    }
                }
            }
        }
    }

