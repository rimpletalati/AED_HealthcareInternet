    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        
        try{
            String age = ageJTextField.getText().trim();
            String weight = weightJTextField.getText().trim();
            String height = heightJTextField.getText().trim();
            String signs = signsJTextArea.getText();
            String test = testPerformedJTextArea.getText();
            String drugs = drugsUsedJTextArea.getText();
            String results = resultsJTextArea.getText();
            String recoveryTime = recoveryTimeJTextArea.getText();
            String comments = additionalCommentsJTextArea.getText();
             
            if (("".equals(age)) ||("".equals(weight)) || ("".equals(height)) || ("".equals(signs)) ||
                    ("".equals(test)) || ("".equals(drugs)) || ("".equals(results)) || ("".equals(recoveryTime))
                    || ("".equals(comments))){
                JOptionPane.showMessageDialog(null,"Please do not keep any values empty.");
                return;
            }
            
            Disease disease = (Disease)diseaseJComboBox.getSelectedItem();
            Stories s = new Stories();
            s.setAge(Integer.parseInt(age));
            s.setWeight(Float.parseFloat(weight));
            s.setHeight(Float.parseFloat(height));
            s.setSigns(signs);
            s.setTestPerformed(test);
            s.setDrugsUsed(drugs);
            s.setResultTreatment(results);
            s.setRecoveryTime(recoveryTime);
            s.setAdditionalComments(comments);
            s.setDisease(disease);
            s.setPatient(patient);
            if(regularCheckupJRadioButton.isSelected()){
                s.setIllnessDiscovered(Stories.regular_checkup);
            }
            if(otherJRadioButton.isSelected()){
                s.setIllnessDiscovered(Stories.others);
            }
            if(didNotFeelWellJRadioButton.isSelected()){
                s.setIllnessDiscovered(Stories.not_feel_Well);
            }
            ecosystem.getPatientForum().getStoriesList().add(s);
            JOptionPane.showMessageDialog(null,"Thank you for sharing your story!");
            ageJTextField.setText("");
            weightJTextField.setText("");
            heightJTextField.setText("");
            signsJTextArea.setText("");
            testPerformedJTextArea.setText("");
            drugsUsedJTextArea.setText("");
            resultsJTextArea.setText("");
            recoveryTimeJTextArea.setText("");
            additionalCommentsJTextArea.setText("");
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Please use numbers for age/weight/height");
        }
    }//GEN-LAST:event_submitJButtonActionPerformed

