    public void populateDisease() {
        diseaseJComboBox.removeAllItems();
            for (Disease d : ecosystem.getManageDiseaseAdmin().getDiseaseList()) {
                diseaseJComboBox.addItem(d);
        }
    }
    
    private void populateTable(Disease d){
        DefaultTableModel dtm = (DefaultTableModel)manageStoriesJTable.getModel();
        dtm.setRowCount(0);
        for(Stories s:ecosystem.getPatientForum().getStoriesList())
        {
            if(d==s.getDisease()){
            Object[]row=new Object[4];
            row[0]=s;
            row[1] = s.getPatient().getFirstName()+" "+s.getPatient().getLastName();
            row[2] = s.getAge();
            row[3] = s.getSubmittedDate();
            dtm.addRow(row);
            }
        }
    }

