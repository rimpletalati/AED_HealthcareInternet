    private void viewStoryJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewStoryJButtonActionPerformed
        
        int selectedRow = manageStoriesJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        Stories s = (Stories) manageStoriesJTable.getValueAt(selectedRow, 0);
       
        ViewStoriesJPanel viewStoriesJPanel = new ViewStoriesJPanel(userProcessContainer,s);
        userProcessContainer.add("viewStoriesJPanel", viewStoriesJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_viewStoryJButtonActionPerformed


