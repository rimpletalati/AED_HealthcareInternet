    private void populatePatients() {
        DefaultTableModel model = (DefaultTableModel) manageTableJTable.getModel();
        model.setRowCount(0);
        for (WorkRequest wr : patient.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof PharmacyWorkRequest) {
                PharmacyWorkRequest pwr = (PharmacyWorkRequest) wr;
                Object[] row = new Object[5];
                row[0] = pwr;
                row[1] = pwr.getSender().getFirstName() + " " + pwr.getSender().getLastName();
                row[2] = pwr.getEnterprise();
                row[3] = pwr.getPharmicistName();
                row[4] = pwr.getStatus();
                model.addRow(row);

            }
        }
    }

