    private void viewDrugsJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDrugsJButtonActionPerformed
        int selectedRow = manageTableJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        PharmacyWorkRequest request = (PharmacyWorkRequest) manageTableJTable.getValueAt(selectedRow, 0);
        drugsJPanel.setVisible(true);
        viewDrugsJTextArea.setText(request.getDrugs().getDrugName());

    }//GEN-LAST:event_viewDrugsJButtonActionPerformed

